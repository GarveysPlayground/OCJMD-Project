package suncertify.onStart;

/**
 * The Enum specifies which modes the application can run in.
 * The application can start in any one of the below 3 modes.
 */
public enum ApplicationMode {

    /** The server mode. */
    SERVER,    
    
    /** The standalone mode. */
    ALONE,
    
    /** The network client mode. */
    NETWORK
}
